async function _tw_fallbackRoomsFromSearch(pageSize, categoryId) {
  const fallbackKeywords = ["just chatting", "valorant", "minecraft", "league"];
  const users = [];
  const seen = new Set();

  for (const keyword of fallbackKeywords) {
    let list = [];
    try {
      list = await _tw_searchChannels(keyword);
    } catch (_) {
      continue;
    }

    for (const user of list) {
      const login = _tw_str(user && user.login);
      if (!login || seen.has(login)) continue;
      seen.add(login);
      users.push(user);
      if (users.length >= pageSize * 2) break;
    }
    if (users.length >= pageSize * 2) break;
  }

  let liveUsers = users.filter(function (user) {
    return !!(user && user.stream);
  });

  if (categoryId && categoryId !== "all" && categoryId !== "root") {
    const filtered = liveUsers.filter(function (user) {
      return _tw_filterByCategory(user.stream, categoryId);
    });
    liveUsers = filtered;
  }

  return liveUsers.slice(0, pageSize).map(function (user) {
    return _tw_toRoomModel(user, user.stream || null);
  });
}

