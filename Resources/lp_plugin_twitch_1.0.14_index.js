function _tw_parseM3U8Variants(m3u8Text, roomId) {
  const lines = _tw_str(m3u8Text)
    .split(/\r?\n/)
    .map(function (line) {
      return _tw_str(line).trim();
    })
    .filter(function (line) {
      return line.length > 0;
    });

  const variants = [];
  for (let i = 0; i < lines.length; i += 1) {
    const line = lines[i];
    if (!line.startsWith("#EXT-X-STREAM-INF:")) continue;

    const attrs = _tw_parseM3U8Attributes(line);
    const urlLine = _tw_str(lines[i + 1]);
    if (!urlLine || urlLine.startsWith("#")) continue;

    variants.push({
      roomId: roomId,
      title: _tw_qualityTitle(attrs, variants.length),
      qn: _tw_int(attrs.BANDWIDTH, 0),
      url: urlLine,
      liveCodeType: "m3u8",
      liveType: _tw_liveType,
      userAgent: _tw_playbackUserAgent,
      headers: _tw_playbackHeaders
    });
  }

  variants.sort(function (a, b) {
    return _tw_int(b.qn, 0) - _tw_int(a.qn, 0);
  });

  return variants;
}

