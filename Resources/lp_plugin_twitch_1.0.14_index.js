async function _tw_fetchDynamicCategories(limit) {
  const maxCount = Math.max(10, Math.min(300, _tw_int(limit, 120)));
  const mergedEdges = [];
  let after = null;
  for (let i = 0; i < 40; i += 1) {
    const batch = await _tw_fetchTopStreams(30, after);
    if (!batch.length) break;
    mergedEdges.push.apply(mergedEdges, batch);
    after = _tw_str(batch[batch.length - 1] && batch[batch.length - 1].cursor);
    if (!after) break;
  }

  const edges = mergedEdges;

  const stats = new Map();
  for (const edge of edges) {
    const game = edge && edge.node && edge.node.game ? edge.node.game : null;
    const gameId = _tw_str(game && game.id);
    if (!gameId) continue;

    const prev = stats.get(gameId) || {
      id: gameId,
      title: _tw_str(game && game.name) || gameId,
      icon: _tw_formatCategoryIcon(game && game.boxArtURL),
      score: 0
    };
    prev.score += 1;
    if (!prev.icon) {
      prev.icon = _tw_formatCategoryIcon(game && game.boxArtURL);
    }
    stats.set(gameId, prev);
  }

  const ranked = Array.from(stats.values())
    .sort(function (a, b) {
      return _tw_int(b.score, 0) - _tw_int(a.score, 0);
    })
    .slice(0, maxCount)
    .map(function (item) {
      return {
        id: item.id,
        title: item.title,
        icon: item.icon,
        biz: ""
      };
    });

  if (ranked.length === 0) return [];
  return ranked;
}

async function _tw_fetchDynamicCategoriesBySearch(limit) {
  const maxCount = Math.max(10, Math.min(300, _tw_int(limit, 120)));
  const searchSeeds = ["a", "e", "i", "o", "u", "s", "t", "r", "n", "c", "m", "l", "g"];

  const result = [];
  const seen = new Set();
  for (const seed of searchSeeds) {
    let data = null;
    try {
      data = await _tw_gql(
        "query($q:String!,$first:Int!){searchCategories(query:$q,first:$first){edges{node{id name displayName avatarURL(width:285)}}}}",
        { q: seed, first: 30 }
      );
    } catch (_) {
      continue;
    }

    const edges = (((data || {}).searchCategories || {}).edges) || [];
    if (!Array.isArray(edges) || edges.length === 0) continue;

    for (const edge of edges) {
      const node = edge && edge.node ? edge.node : null;
      const id = _tw_str(node && node.id);
      const title = _tw_str((node && (node.displayName || node.name)) || "").trim();
      if (!id || !title || seen.has(id)) continue;
      // Skip noisy one-letter results from fuzzy search.
      if (title.length <= 1) continue;

      seen.add(id);
      result.push({
        id: id,
        title: title,
        icon: _tw_formatCategoryIcon(node && node.avatarURL),
        biz: ""
      });
      if (result.length >= maxCount) break;
    }

    if (result.length >= maxCount) break;
  }

  if (result.length === 0) return [];
  return result;
}

